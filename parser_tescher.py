import os                  # модуль для работы с файловой системой (создать папку, проверить существование файла)
import json                # модуль для чтения и записи JSON-файлов (наш кеш-индекс хранится в JSON)
from typing import Any     # Any — специальный тип-заглушка, говорит Pylance «тут может быть любое значение»
import requests            # сторонняя библиотека для HTTP-запросов (скачать страницу, отправить запрос к API)


# ── Константы (настройки, которые не меняются в процессе работы) ──────────────

BASE_URL   = 'https://spo-13.mskobr.ru'          # корневой адрес сайта — будем добавлять к нему все пути
PAGE_PATH  = '/uchashimsya/raspisanie-kanikuly'   # URL-путь страницы расписания на сайте
KORPUS     = 'Корпус Ярославский'                 # название корпуса, файлы которого нас интересуют
CACHE_FILE = 'cache_index.json'                   # имя файла-кеша, в котором храним что уже скачали


# ══════════════════════════════════════════════════════════════════════════════
# ФУНКЦИЯ 1 — parse_schedule_files
# Назначение: спрашивает API сайта и возвращает список файлов расписания
#             (только для нужного корпуса). Никаких файлов не скачивает —
#             только собирает информацию о них (ссылки, курс, расширение).
# Возвращает: список словарей, каждый словарь — один файл расписания
# ══════════════════════════════════════════════════════════════════════════════

def parse_schedule_files() -> list[dict[str, Any]]:

    # ── Шаг 1: узнаём числовой ID страницы ───────────────────────────────────
    # Сайт работает как SPA (Vue.js) — данные грузит через внутренний API,
    # а не хранит в HTML. Сначала нужно получить числовой ID страницы по её пути.
    # requests.post — отправляем POST-запрос (как отправка формы).
    # json={'path': PAGE_PATH} — тело запроса в формате JSON: {"path": "/uchashimsya/..."}
    #
    # ── КАК ПРОВЕРИТЬ ЭТО В БРАУЗЕРЕ ВРУЧНУЮ ────────────────────────────────
    # 1. Открыть https://spo-13.mskobr.ru/uchashimsya/raspisanie-kanikuly
    # 2. Нажать F12 → вкладка «Network» (Сеть)
    # 3. Обновить страницу (F5), в поиске по запросам написать «page»
    # 4. Найти запрос POST /v1/api/page/id — кликнуть на него
    # 5. Вкладка «Payload» покажет тело запроса: {"path": "/uchashimsya/raspisanie-kanikuly"}
    # 6. Вкладка «Response» покажет ответ: {"data": {"id": 30190, "title": "Расписание..."}}
    # Либо проверить прямо в браузере через консоль (F12 → Console):
    #   fetch('/v1/api/page/id', {method:'POST', headers:{'Content-Type':'application/json'},
    #         body: JSON.stringify({path:'/uchashimsya/raspisanie-kanikuly'})})
    #   .then(r=>r.json()).then(console.log)
    response = requests.post(BASE_URL + '/v1/api/page/id', json={'path': PAGE_PATH})

    # response.json() — превращаем ответ сервера (строку JSON) в словарь Python
    # ['data']['id'] — берём вложенное поле: ответ выглядит как {"data": {"id": 30190, ...}}
    page_id = response.json()['data']['id']       # page_id = 30190

    # ── Шаг 2: получаем дерево папок и файлов ────────────────────────────────
    # Теперь, зная ID страницы, запрашиваем все папки и файлы, прикреплённые к ней.
    # f'/v1/api/folder_and_file/list/{page_id}' — f-строка, подставляет page_id в URL
    # requests.get — обычный GET-запрос (как открыть ссылку в браузере)
    #
    # ── КАК ПРОВЕРИТЬ ЭТО В БРАУЗЕРЕ ВРУЧНУЮ ────────────────────────────────
    # Способ 1 — просто вставить URL в адресную строку браузера (это GET-запрос):
    #   https://spo-13.mskobr.ru/v1/api/folder_and_file/list/30190
    #   Браузер покажет JSON с деревом папок и файлов прямо на экране.
    # Способ 2 — через DevTools (F12 → Network), обновить страницу расписания,
    #   найти запрос «list/30190», вкладка Response покажет полную структуру:
    #   {"data": {
    #     "folders": [
    #       {"title": "Расписание учебных занятий", "folders": [
    #         {"title": "Корпус Ярославский", "files": [
    #           {"id": 8491, "title": "1 курс", "src": "/attach_files/...", "ext": "xlsx"},
    #           ...
    #         ]}
    #       ]}
    #     ]
    #   }}
    response = requests.get(BASE_URL + f'/v1/api/folder_and_file/list/{page_id}')

    # Из ответа берём список папок верхнего уровня.
    # Структура ответа: {"data": {"folders": [...], "files": [...]}}
    folders = response.json()['data']['folders']  # список всех папок на странице

    # ── Шаг 3: ищем папку «Расписание учебных занятий» ───────────────────────
    # На странице несколько папок (каникулы, расписание и др.) — нужна нужная.
    schedule_folder: dict[str, Any] | None = None  # переменная пока пустая (None = ничего нет)

    for folder in folders:                          # перебираем каждую папку из списка
        # str(...).lower() — приводим к нижнему регистру для сравнения без учёта заглавных букв
        # 'in' — проверяем, содержит ли название папки нужную подстроку
        if 'расписание учебных' in str(folder['title']).lower():
            schedule_folder = folder               # нашли нужную папку — сохраняем
            break                                  # прекращаем цикл, дальше искать не нужно

    # Если папка так и не найдена — возвращаем пустой список, дальше идти некуда
    if schedule_folder is None:
        return []

    # ── Шаг 4: внутри папки расписания ищем нужный корпус ────────────────────
    files: list[dict[str, Any]] = []               # пустой список — сюда будем добавлять файлы

    # schedule_folder['folders'] — список подпапок внутри «Расписания»:
    # [«Корпус Бибирево», «Корпус Шенкурский», «Корпус Ярославский», ...]
    for korpus in schedule_folder['folders']:

        # Пропускаем все корпуса, кроме нужного (константа KORPUS = 'Корпус Ярославский')
        if korpus['title'] != KORPUS:
            continue                               # continue — перейти к следующему элементу цикла

        # Перебираем файлы внутри нужного корпуса (1 курс, 2 курс, 3 курс, 4 курс)
        for f in korpus['files']:

            # f['title'] содержит строку вида "1 курс", "2 курс" и т.д.
            # str(f['title'])[0] — берём первый символ строки: "1", "2", "3", "4"
            # int(...) — превращаем символ в число: "1" → 1
            course = int(str(f['title'])[0])       # номер курса: 1, 2, 3 или 4

            # Добавляем словарь с информацией о файле в общий список
            files.append({
                'file_id': int(f['id']),           # уникальный числовой ID файла на сайте
                'course':  course,                  # номер курса (1–4)
                'src':     str(f['src']),           # путь к файлу на сервере, напр. /attach_files/upload_users_files/6998081244da6.xlsx
                                                    # именно этот путь МЕНЯЕТСЯ при загрузке нового файла
                'ext':     str(f.get('ext', 'xlsx')), # расширение файла (xlsx или zip); если нет — по умолчанию xlsx
                'url':     BASE_URL + str(f['src']), # полный URL для скачивания = сайт + путь к файлу
            })

    return files                                   # возвращаем собранный список файлов


# ══════════════════════════════════════════════════════════════════════════════
# ФУНКЦИЯ 2 — is_new_file
# Назначение: проверяет, изменился ли файл с момента последнего скачивания.
# Принцип:    при каждой загрузке нового расписания на сайт — путь src меняется
#             (генерируется новое имя, например 6998081244da6.xlsx → 6a01d0662714b.xlsx).
#             Мы запоминаем последний src в cache_index.json и сравниваем с текущим.
# Принимает:  file_id — ID файла, src — текущий путь файла на сервере
# Возвращает: True — файл новый/изменился, False — файл не менялся
# ══════════════════════════════════════════════════════════════════════════════

def is_new_file(file_id: int, src: str) -> bool:

    # Если файл кеша ещё не существует — значит мы ничего раньше не скачивали,
    # все файлы считаются новыми
    if not os.path.exists(CACHE_FILE):
        return True                                # True = «да, файл новый»

    # Открываем файл кеша для чтения
    # 'with open(...) as f:' — безопасное открытие файла: закроется автоматически
    # encoding='utf-8' — кодировка, чтобы корректно читать кириллицу
    with open(CACHE_FILE, encoding='utf-8') as f:
        cache = json.load(f)                       # json.load — читаем JSON из файла в словарь Python
                                                   # cache выглядит так: {"8491": "/attach_files/...", "8494": "..."}

    # cache.get(str(file_id)) — ищем в словаре по ключу (ID файла как строка)
    # .get() в отличие от [] не выбросит ошибку если ключа нет — вернёт None
    # Если сохранённый src не совпадает с текущим — файл изменился → True
    return bool(cache.get(str(file_id)) != src)


# ══════════════════════════════════════════════════════════════════════════════
# ФУНКЦИЯ 3 — cache_file
# Назначение: скачивает файл и сохраняет его в папку c1/c2/c3/c4,
#             затем записывает текущий src в кеш-индекс (cache_index.json).
# Принимает:  file_id, course, src, url, ext — всё из словаря, собранного в parse_schedule_files
# Возвращает: строку с путём к сохранённому файлу (например 'c1\kurs1.xlsx')
# ══════════════════════════════════════════════════════════════════════════════

def cache_file(file_id: int, course: int, src: str, url: str, ext: str) -> str:

    # Формируем имя папки: course=1 → 'c1', course=2 → 'c2' и т.д.
    folder = f'c{course}'                          # f-строка подставляет номер курса

    # Создаём папку если её нет. exist_ok=True — не выдаёт ошибку если папка уже есть
    os.makedirs(folder, exist_ok=True)

    # Собираем полный путь к файлу: 'c1' + '\' + 'kurs1.xlsx' = 'c1\kurs1.xlsx'
    # os.path.join корректно соединяет части пути (учитывает слеши Windows/Linux)
    file_path: str = os.path.join(folder, f'kurs{course}.{ext}')

    # Скачиваем файл по URL. requests.get возвращает объект ответа
    response = requests.get(url)

    # Открываем файл для записи в бинарном режиме ('wb' = write binary)
    # response.content — тело ответа в виде байтов (бинарные данные Excel-файла)
    with open(file_path, 'wb') as f:
        f.write(response.content)                  # записываем байты файла на диск

    # ── Обновляем кеш-индекс ──────────────────────────────────────────────────
    # Читаем существующий кеш (если файл есть) или начинаем с пустого словаря
    cache = {}                                     # начальное значение — пустой словарь
    if os.path.exists(CACHE_FILE):                 # проверяем, существует ли файл кеша
        with open(CACHE_FILE, encoding='utf-8') as f:
            cache = json.load(f)                   # загружаем уже сохранённые записи

    # Записываем/обновляем src для этого файла
    # str(file_id) — ключ словаря (ID как строка, потому что JSON-ключи всегда строки)
    # src — текущий путь файла на сервере, по нему будем определять обновление в следующий раз
    cache[str(file_id)] = src

    # Сохраняем обновлённый словарь обратно в JSON-файл
    # ensure_ascii=False — сохраняет кириллицу как есть, не заменяет на \uXXXX
    # indent=2 — форматирует JSON с отступами, чтобы файл был читаем
    with open(CACHE_FILE, 'w', encoding='utf-8') as f:
        json.dump(cache, f, ensure_ascii=False, indent=2)

    return file_path                               # возвращаем путь к сохранённому файлу


# ══════════════════════════════════════════════════════════════════════════════
# ТОЧКА ВХОДА — код ниже выполняется при запуске скрипта
# ══════════════════════════════════════════════════════════════════════════════

# Вызываем функцию парсинга — получаем список словарей с информацией о файлах
files = parse_schedule_files()

# Выводим сколько файлов нашли (для Ярославского корпуса должно быть 4: курсы 1–4)
print(f'Найдено файлов ({KORPUS}): {len(files)}\n')

# Перебираем каждый найденный файл
for f in files:

    # Проверяем: файл новый или уже скачан?
    # f['file_id'] — ID файла, f['src'] — текущий путь на сервере
    if is_new_file(f['file_id'], f['src']):

        # Файл новый — скачиваем и сохраняем в папку c{course}
        # Передаём все нужные данные из словаря в функцию cache_file
        path = cache_file(f['file_id'], f['course'], f['src'], f['url'], f['ext'])

        # Сообщаем куда сохранили и с какого URL скачали
        print(f'Скачан: {path}  ({f["url"]})')

    else:
        # Файл не изменился — пропускаем, не тратим трафик
        print(f'Без изменений: c{f["course"]}/kurs{f["course"]}.{f["ext"]}')
